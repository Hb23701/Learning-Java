public class ArrayofFiveNum {
    public static void main(String[] args) {
        int[] num = new int[5];
        num[0] = 0;
        num[1] = 3;
        num[2] = 6;
        num[3] = 9;
        num[4] = 12;
        for (int i = 0; i<num.length;i++) {
            System.out.println(num[i]);
        }
    }
}
